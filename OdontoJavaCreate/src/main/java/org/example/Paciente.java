package org.example;

public class Paciente {
    public String nome;
    public String email;
    public String telefone;
    public String endereco;
    public String dataN;

    public Paciente(String nome, String email, String telefone, String endereco, String dataN) {
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.endereco = endereco;
        this.dataN = dataN;
    }
}
